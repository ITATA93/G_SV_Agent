def generate_catalog_md(services: list[dict]) -> str:
    today = date.today().isoformat()

    lines = [
        "---",
        "depends_on:",
        "  - configs/services.yml",
        "impacts:",
        "  - src/health.py",
        "  - scripts/verify-services.sh",
        "---",
        "",
        "# Service Catalog — GEN_OS Infrastructure",
        "",
        f"> Auto-generated by `scripts/sync_service_catalog.py` on {today}",
        f"> Source of truth: `configs/services.yml`",
        "> **Do not edit manually** — run `python scripts/sync_service_catalog.py`",
        "",
        f"**Total services: {len(services)}** "
        f"(deployed: {sum(1 for s in services if s.get('deployed'))})",
        "",
    ]

    # Group by category
    by_category: dict[str, list[dict]] = defaultdict(list)
    for svc in services:
        by_category[svc.get("category", "Other")].append(svc)

    # Summary table
    lines.append("## Summary by Category")
    lines.append("")
    lines.append("| Category | Count | Services |")
    lines.append("|----------|-------|----------|")
    for category, svcs in by_category.items():
        names = ", ".join(s["name"] for s in svcs)
        lines.append(f"| {category} | {len(svcs)} | {names} |")
    lines.append("")

    # Detailed table
    lines.append("## Full Service Registry")
    lines.append("")
    lines.append("| # | Name | Category | External URL | Port | VM | Version | Status | Description |")
    lines.append("|---|------|----------|-------------|------|-----|---------|--------|-------------|")

    for i, svc in enumerate(services, 1):
        name = svc.get("name", "?")
        cat = svc.get("category", "?")
        url = svc.get("url_external") or "—"
        port = svc.get("port_internal", "—")
        vm = svc.get("vm", "—")
        ver = svc.get("version", "—")
        deployed = "Deployed" if svc.get("deployed") else "Pending"
        desc = svc.get("description", "—")
        lines.append(f"| {i} | **{name}** | {cat} | {url} | {port} | {vm} | {ver} | {deployed} | {desc} |")

    lines.append("")

    # NocoBase instances detail section
    nocobase_svcs = [s for s in services if "nocobase" in s.get("name", "").lower() or "mira" in s.get("name", "").lower()]
    if nocobase_svcs:
        lines.append("## NocoBase Instances Detail")
        lines.append("")
        lines.append("| Instance | URL | Port | Version | DB | Deployed |")
        lines.append("|----------|-----|------|---------|-----|----------|")
        for svc in nocobase_svcs:
            name = svc.get("name", "?")
            url = svc.get("url_external") or "—"
            port = svc.get("port_internal", "—")
            ver = svc.get("version", "—")
            db = svc.get("db_name", "—")
            deployed = "Yes" if svc.get("deployed") else "No"
            lines.append(f"| {name} | {url} | {port} | {ver} | {db} | {deployed} |")
        lines.append("")

    # Port mapping section
    lines.append("## Internal Port Map (VM101 + VM100)")
    lines.append("")
    lines.append("| Port | Service | VM |")
    lines.append("|------|---------|-----|")
    port_entries = sorted(
        [(svc.get("port_internal", 0), svc["name"], svc.get("vm", "?"))
         for svc in services if svc.get("port_internal")],
        key=lambda x: (str(x[2]), x[0])
    )
    for port, name, vm in port_entries:
        lines.append(f"| {port} | {name} | {vm} |")
    lines.append("")

    return "\n".join(lines)


