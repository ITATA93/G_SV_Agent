#!/usr/bin/env python3
"""
sync_service_catalog.py — Auto-genera artefactos desde configs/services.yml

Lee el registro canónico de servicios y regenera:
  1. scripts/verify-services.sh  — Health check bash script
  2. docs/SERVICE_CATALOG.md     — Tabla Markdown de todos los servicios

Con --propagate, lee configs/consumers.yml y propaga credenciales
del .env maestro de G_SV_Agent a los .env de proyectos satélite.

Uso:
  python scripts/sync_service_catalog.py                    (sync catalog)
  python scripts/sync_service_catalog.py --propagate        (sync + propagate credentials)
  python scripts/sync_service_catalog.py --propagate-only   (only propagate, skip catalog)
  python scripts/sync_service_catalog.py --dry-run          (preview, no writes)

Registrado en: docs/library/scripts.md
"""

from __future__ import annotations

import argparse
import sys
from collections import defaultdict
from datetime import date
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
SERVICES_YAML = ROOT / "configs" / "services.yml"
CONSUMERS_YAML = ROOT / "configs" / "consumers.yml"
MASTER_ENV = ROOT / ".env"
VERIFY_SCRIPT = ROOT / "scripts" / "verify-services.sh"
CATALOG_MD = ROOT / "docs" / "SERVICE_CATALOG.md"


# ---------------------------------------------------------------
# YAML parser (no external deps)
# ---------------------------------------------------------------

def parse_yaml_value(val: str):
    if not val or val == "null":
        return None
    if val in ("true", "True"):
        return True
    if val in ("false", "False"):
        return False
    if (val.startswith('"') and val.endswith('"')) or \
       (val.startswith("'") and val.endswith("'")):
        return val[1:-1]
    try:
        return int(val)
    except ValueError:
        pass
    return val


def load_services_yaml(path: Path) -> list[dict]:
    if not path.exists():
        print(f"ERROR: {path} not found", file=sys.stderr)
        sys.exit(1)

    services: list[dict] = []
    current: dict | None = None
    text = path.read_text(encoding="utf-8")

    for line in text.splitlines():
        stripped = line.strip()
        if not stripped or stripped.startswith("#"):
            continue
        # Top-level list items start at column 0 with "- "
        indent = len(line) - len(line.lstrip())
        if stripped.startswith("- ") and indent == 0:
            if current is not None:
                services.append(current)
            current = {}
            kv = stripped[2:].strip()
            if ":" in kv:
                key, val = kv.split(":", 1)
                current[key.strip()] = parse_yaml_value(val.strip())
        elif ":" in stripped and current is not None and indent > 0:
            # Skip nested list items (e.g. extra_containers sub-items)
            if stripped.startswith("- "):
                continue
            key, val = stripped.split(":", 1)
            current[key.strip()] = parse_yaml_value(val.strip())

    if current is not None:
        services.append(current)

    return services


# ---------------------------------------------------------------
# Generator: verify-services.sh
# ---------------------------------------------------------------

VERIFY_HEADER = '''#!/usr/bin/env bash
# ============================================================
# verify-services.sh
# Health check for all GEN_OS remote services
#
# AUTO-GENERATED by scripts/sync_service_catalog.py
# from configs/services.yml — DO NOT EDIT MANUALLY
#
# Usage: bash scripts/verify-services.sh
# ============================================================

set -uo pipefail

echo "============================================"
echo " GEN_OS - Service Health Check"
echo " Date: $(date '+%Y-%m-%d %H:%M:%S')"
echo "============================================"
echo ""

UP=0
DOWN=0
TOTAL=0

check_service() {
  local name="$1"
  local url="$2"
  TOTAL=$((TOTAL + 1))

  STATUS=$(curl -sf -o /dev/null -w "%{http_code}" --connect-timeout 5 --max-time 10 "$url" 2>/dev/null || echo "000")

  if [[ "$STATUS" =~ ^(200|301|302|303|307|308)$ ]]; then
    echo "  [UP]   $name ($url) - HTTP $STATUS"
    UP=$((UP + 1))
  else
    echo "  [DOWN] $name ($url) - HTTP $STATUS"
    DOWN=$((DOWN + 1))
  fi
}
'''

VERIFY_FOOTER = '''
echo ""
echo "============================================"
echo " Results: $UP UP / $DOWN DOWN / $TOTAL total"
echo " Readiness: $(( (UP * 100) / TOTAL ))%"
echo "============================================"
'''


def generate_verify_script(services: list[dict]) -> str:
    lines = [VERIFY_HEADER]

    # Group by category
    by_category: dict[str, list[dict]] = defaultdict(list)
    for svc in services:
        if svc.get("deployed") and svc.get("url_external"):
            by_category[svc.get("category", "Other")].append(svc)

    for category, svcs in by_category.items():
        lines.append(f'echo "--- {category} ---"')
        for svc in svcs:
            name = svc["name"]
            url = svc["url_external"]
            # Pad name for alignment
            lines.append(f'check_service "{name}"  "{url}"')
        lines.append('echo ""')

    lines.append(VERIFY_FOOTER)
    return "\n".join(lines)


